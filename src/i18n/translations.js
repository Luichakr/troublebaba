// Browser lang code → site lang
export function detectBrowserLang() {
  const langs = navigator.languages || [navigator.language];
  const supported = ['uk', 'ru', 'pl', 'en', 'es', 'de', 'fr', 'it', 'pt'];
  for (const l of langs) {
    const code = l.toLowerCase().split('-')[0];
    if (supported.includes(code)) return code;
  }
  return null;
}

// Country code → site lang
export function countryToLang(country) {
  const c = (country || '').toUpperCase();
  if (c === 'UA') return 'uk';
  if (['RU', 'BY', 'KZ'].includes(c)) return 'ru';
  if (c === 'PL') return 'pl';
  if (['ES', 'MX', 'AR', 'CO', 'CL', 'PE'].includes(c)) return 'es';
  if (['DE', 'AT', 'CH'].includes(c)) return 'de';
  if (['FR', 'BE'].includes(c)) return 'fr';
  if (c === 'IT') return 'it';
  if (['PT', 'BR'].includes(c)) return 'pt';
  return 'en';
}
